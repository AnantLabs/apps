TARGET=$2

TSIZE="$(wc -l $TARGET | awk '{print $1}')"
QSIZE="$(( $TSIZE - 2 ))"

tail "-$QSIZE" "$TARGET" > "$TARGET.tail"
head -2 "$TARGET" > "$TARGET.head"
cat "$TARGET.head" "$HEADER" "$TARGET.tail" > "$TARGET"
rm "$TARGET.head" "$TARGET.tail"